from compiler.ast import *

# -------------------------------------------------------------------------------------------------
# LABEL
# -------------------------------------------------------------------------------------------------

class Label(Node):

    def __init__(self, name, lineno=None):

        self.name = name
        self.lineno = lineno

    def getChildren(self):
        return self.name,

    def getChildNodes(self):
        return ()

    def __repr__(self):
        return "Label(%s)" % (repr(self.name))

